    private void bttnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttnSaveActionPerformed
        if(txtName.getText().length() == 0){
            JOptionPane.showMessageDialog(null, "Please fill up the name", "Alert", JOptionPane.WARNING_MESSAGE);
        }else if(MOP.getSelectedItem().toString().equals("Cash")){
            if(txtCash.getText().length() == 0){
                JOptionPane.showMessageDialog(null, "Please enter cash", "Alert", JOptionPane.WARNING_MESSAGE);
            }else{
                saveDataToFile();
            }
        }else{
            saveDataToFile();
        }
        
    }//GEN-LAST:event_bttnSaveActionPerformed

   private void saveDataToFile() {
        String fileName = "ordersData.txt"; // Name of the text file
        
        String cusName = txtName.getText(); 
        String orderNum = lblorderNum.getText();
        String orderDate = lblDate.getText(); 
        String orderTime = lblTime.getText();
        String transacMode = (String)MOP.getSelectedItem(); 
        String total = lblTotal.getText();
        String m1 = txtM1.getText(), m2 = txtM2.getText(), m3 = txtM3.getText(), m4 = txtM4.getText(), m5 = txtM5.getText();
        String s1 = txtS1.getText(), s2 = txtS2.getText(), s3 = txtS3.getText(), s4 = txtS4.getText(), s5 = txtS5.getText();
        String d1 = txtD1.getText(), d2 = txtD2.getText(), d3 = txtD3.getText(), d4 = txtD4.getText(), d5 = txtD5.getText();
        String b1 = txtB1.getText(), b2 = txtB2.getText(), b3 = txtB3.getText(), b4 = txtB4.getText(), b5 = txtB5.getText();

        try {
            FileWriter fileWriter = new FileWriter(fileName, true);
            // Encrypt the data
            try (BufferedWriter bufferedWriter = new BufferedWriter(fileWriter)) {
                // Encrypt the data
                String encryptedData = encrypt(orderNum + "," + cusName + "," + orderDate + "," + orderTime + "," + total + "," + transacMode + "," + m1 + "," + m2 + "," + m3 + "," + m4 + "," + m5 + "," + s1 + "," + s2 + "," + s3 + "," + s4 + "," + s5 + "," + d1 + "," + d2 + "," + d3 + "," + d4 + "," + d5 + "," + b1 + "," + b2 + "," + b3 + "," + b4 + "," + b5 + "," + "Pending");
                // Append the encrypted data to the file
                bufferedWriter.write(encryptedData);
                bufferedWriter.newLine();
                // Close the writer
            }

            JOptionPane.showMessageDialog(this, "Data has been saved to the file.");

        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Error writing to the file: " + ex.getMessage());
        }
        
        Menu menu = new Menu();
        menu.setVisible(true);
        menu.pack();
        menu.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        this.dispose();
    }
   
   private String encrypt(String data) {
        try {
            cipher.init(Cipher.ENCRYPT_MODE, secretKey);
            byte[] encryptedBytes = cipher.doFinal(data.getBytes(StandardCharsets.UTF_8));
            return Base64.getEncoder().encodeToString(encryptedBytes);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error encrypting data: " + ex.getMessage());
            return null;
        }
    }
