    private void txtCashActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtCashActionPerformed
        
        totalAmt = Double.parseDouble(lblTotal.getText());
        double cash  = Double.parseDouble(txtCash.getText());
        
        if(cash > totalAmt){
            lblChange.setEnabled(true);
            txtChange.setEnabled(true);
            double change = cash - totalAmt;
            DecimalFormat df = new DecimalFormat("#.00");
            String strChange = df.format(change);
            txtChange.setText(strChange);
        } else if(totalAmt > cash){
            JOptionPane.showMessageDialog(null, "Invalid cash amount!", "Alert", JOptionPane.WARNING_MESSAGE);
            txtCash.setText(null);
        }
    }//GEN-LAST:event_txtCashActionPerformed

